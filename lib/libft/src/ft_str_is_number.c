int 	ft_str_is_number(char *str)
{

	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[0] == '-')
			i++;
		if (str[i] >= '0' && str[i] <= '9')
			i++;
		else
			return (0);
	}
	return (1);
}