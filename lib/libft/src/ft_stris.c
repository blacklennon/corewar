#include "libft.h"

int			ft_stris(char *str, int (*f)(int c))
{
	if (str == NULL || *str == '\0')
		return (0);
	while (*str)
	{
		if (!f((int)*str))
			return (0);
		str++;
	}
	return (1);
}
